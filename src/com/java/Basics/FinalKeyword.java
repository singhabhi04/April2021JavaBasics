package com.java.Basics;

public class FinalKeyword {

	public static void main(String[] args) {
		// final is a modifier in java , which is applicable for classes ,methods and variable
		// If a method is final,then we can't override that method in child class
		//if a class is declared as final,then we can't extend that class(Inheritance is not possible for child class)
		
		// if a variable is declared final we cannot re-assign the value
		
		final int a =10;
		//a=30;
		final int weekDays= 7;

	}

}
